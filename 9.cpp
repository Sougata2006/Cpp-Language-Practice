#include <iostream>
using namespace std;
int main()
{

    // loops----

    //---------------------------------------------------------

    //  while loop-----------
    int num = 0;
    while (num < 10)
    {
        cout << "the num is" << num << "\n";
        num++;
    }
    // -------------------------------------------------------

    //  do while loop----------------
    int num = 0;
    do
    {
        cout << "bb" << num << "\n";
        num++;
    } while (num < 10);
    //---------------------------------------------------------

    // for loop----------------------
    for (int i = 0; i < 10; i++)
    {
        cout << ("let's goooooo!!") << i << "\n";
    }
    //---------------------------------------------------------

    return 0;
}
